fav_colour = input('What is your favourite colour?: ').lower()


#Use the 'If' keyword to start an if statement followed by a condition to evaluate
if fav_colour == 'red':
    print('That is my favourite color too!I love red')

elif fav_colour == 'yellow':
    print('That is my favourite colour too!Everyone loves yellow')

elif fav_colour == 'blue':
    print('That is my favourite colour too! Blue is the best')
 
else:
    print("I don't like your favourite colour.")


